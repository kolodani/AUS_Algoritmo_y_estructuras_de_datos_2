-- Modelos basados en:

-- Maquinas: Considera el costo de las instrucciones ejecutadas por el algoritmo en la maquina subyacente.

-- Lenguajes: considera una funcion de costo sobre el lenguaje de implementacion del algoritmo.

-- * Ambos ignoran factores constantes que dependan del hardware.
-- * Los modelos basados en maquinas son mas precisos,
-- * pero sus analisis es complejo y poco expresivo.
-- * Los modelos basados en lenguajes son mas simples,
-- * pero menos precisos en algunos casos.
