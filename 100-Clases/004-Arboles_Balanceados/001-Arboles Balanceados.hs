-- En los arboles binarios de busqueda:

-- 1. Las operaciones de busqueda, insercion y borrado son del orden de la altura del arbol.
-- 2. Si insertamos datos en orden, la altura del arbol es maxima y los rendimientos son los de una lista.
-- 3. Si mantenemos la altura lo mas bajo posible nuestros algoritmos son mas rapidos
-- 4. Para lograr esto debemos mantener nuestros arboles balanceados.
-- 5. La altura del arbol debe estar en el orden del log n.
