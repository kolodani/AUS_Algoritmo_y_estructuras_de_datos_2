-- Definicion (Arboles)
-- Un arbol es una estructura de datos donde la informacion almacenada
-- se conecta sin formar ciclos mediante ramas. Esas conexiones cumple la
-- relacion padre-hijo. El primer nodo del arbol se lo denomina raiz y a
-- los ultimos se los llama hojas. Todos los demas puntos de ramificacion
-- se denominaran nodos.
