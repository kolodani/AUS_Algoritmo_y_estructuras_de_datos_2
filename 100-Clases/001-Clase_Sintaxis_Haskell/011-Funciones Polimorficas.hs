    Funciones Polimorficas
-}

-- una funcion es polimorfica si su tipo contiene una o mas variables de tipo
-- length :: [a] -> Int

-- en este caso a es una variable de tipo que puede asumir cualquier tipo valido

-- Para cualquier tipo a la funcion length es la misma

-- las variables de tipo pueden ser instanciadas a otros tipos
-- length [False, True]    a = Bool
-- length ['a', 'b']       a = Char

-- las variables de tipo se escriben con minuscula, a, b, c, etc.
