{-
    Operadores de Secciones
-}

-- un operador escrito entre sus dos argumentos (infijo), puede ser escrito antes de sus dos argumentos (prefijo) usando parentesis:

-- > (+) 1 2
-- 3

-- tambien uno de los argumentos puede ser incluido en los parentesis
-- > (1 +) 2
-- 3
-- > (+ 2) 1
-- 3

-- en general, dando un operador cualquiera, entonces las funciones de la forma (+), (1+), (+1) son llamadas secciones